#ifndef FLOWCORE_HASHT_H
#define FLOWCORE_HASHT_H

#include "FlowCore/Library.h"

// -----------------------------------------------------------------------------
//  Struct FHashT
// -----------------------------------------------------------------------------

template <typename T>
struct FHashT
{
	size_t operator()(const T& obj) const
	{
		return obj.hashCode();
	}
};
	
// -----------------------------------------------------------------------------

#endif // FLOWCORE_HASHT_H